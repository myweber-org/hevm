module DataProcessor where

filterAndTransform :: (Int -> Bool) -> (Int -> Int) -> [Int] -> [Int]
filterAndTransform predicate transformer = map transformer . filter predicate

processEvenSquares :: [Int] -> [Int]
processEvenSquares = filterAndTransform even (\x -> x * x)

sumProcessedList :: (Int -> Int) -> [Int] -> Int
sumProcessedList processor = foldr (+) 0 . map processor

main :: IO ()
main = do
    let numbers = [1..10]
    putStrLn "Original list:"
    print numbers
    
    putStrLn "\nSquares of even numbers:"
    let evenSquares = processEvenSquares numbers
    print evenSquares
    
    putStrLn "\nSum of doubled numbers:"
    let sumDoubled = sumProcessedList (*2) numbers
    print sumDoubled