validateInput :: [Int] -> Maybe [Int]
validateInput xs
    | null xs = Nothing
    | any (< -100) xs = Nothing
    | any (> 100) xs = Nothing
    | otherwise = Just xs

safeDataProcessing :: [Int] -> Maybe Int
safeDataProcessing xs = do
    validInput <- validateInput xs
